package battleship;

import java.util.*;

public class Position{
	
	private int startX;
	private int startY;
	private int length;
	private int direction;
	
	public Position(int x, int y, int l, int dir)
	{
		startX=x;
		startY=y;
		length=l;
		direction = dir;
	}
	
	public int getXpos()
	{
		return getXpos();
	}
	
	public int getYpos()
	{
		return getYpos();
	}
	
	public int getDir()
	{
		return direction;
	}
	
}
